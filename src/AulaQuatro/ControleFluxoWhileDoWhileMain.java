package AulaQuatro;

public class ControleFluxoWhileDoWhileMain {
    public static void main(String[] args) {

        int x = 10;

        while(x<10){
            System.out.println("Item X: " + x);
            x++;
        }

        int y = 10;

        do{
            System.out.println("Item Y: " + y);
            y++;
        }while(y<10);
    }
}
